    private void add2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add2ActionPerformed
        // TODO add your handling code here:
        if (ing2.getText() == null || ing2.getText().isEmpty())
               return;
        String friendName = ing2.getText();
        ing2.setText("");
        User friend = new User(friendName);
        ArrayList temp = new ArrayList();
        if (db.loadIngredients(friend) == null)
            return;
        else {
            temp = db.loadIngredients(friend);
            friend.replaceInventory(temp);
        }
        listSharedIngredients(friend);
        
    }//GEN-LAST:event_add2ActionPerformed

