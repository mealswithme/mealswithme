    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        if (jTextField1.getText() == null || jTextField1.getText().isEmpty())
               return;
        username = jTextField1.getText();
        user = new User(username);
        ArrayList temp = new ArrayList();
        jTextField1.setText("");
        if (db.loadIngredients(user) != null){
            //return;
        //else {
            temp = db.loadIngredients(user);
            user.replaceInventory(temp);
        }
        for (int i = 0; i < user.getInventory().size(); i++) {
            jTextPane1.setText(jTextPane1.getText() + user.getInventory().get(i) + "\n");
        }    
        jLabel2.setText("Signed in as: " + user.getUsername());
        
        //System.out.println(user.getUsername());
    }//GEN-LAST:event_loginActionPerformed

